# ELIF Statement / else if statement

nama = input("Nama kamu siapa? ")

if nama == "ucup": # kondisi 1
    print("Hai Ganteng ucup!!!") # aksi true 1
elif nama == "otong": # kondisi 2
    print("Hai Ganteng otong!!!") # aksi true 2
elif nama == "gandrung": # kondisi 3
    print("Hai Ganteng gandrung!!!") # aksi true 3
else:
    print("au ah gak kenallll")

