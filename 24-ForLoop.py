# For Loop (Perulangan)

# for kondisi: 
#     aksi

# Dengan list
angka2_list = [0,3,4,8,1] # ini adalah list
print(angka2_list)

for i in angka2_list:
    print(f"i sekarang -> {i}")

print("Akhir program 1\n")

# ini dengan range
